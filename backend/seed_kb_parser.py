def seed_knowledge_base():
    print("🌱 Начинаем наполнение базы знаний...")
    Base.metadata.create_all(bind=engine)
    db = SessionLocal()

    try:
        added_count = 0
        for item in KNOWLEDGE_DATA:
            # Проверяем, нет ли уже такой статьи
            exists = db.query(KnowledgeBase).filter(KnowledgeBase.title == item["title"]).first()
            if not exists:
                kb = KnowledgeBase(**item)
                db.add(kb)
                added_count += 1
                print(f"✅ Добавлена: {item['title']}")
            else:
                print(f"⏭️ Уже существует: {item['title']}")

        db.commit()
        print(f"\n🎉 Готово! Всего добавлено/обновлено статей: {added_count}")
    except Exception as e:
        db.rollback()
        print(f"❌ Ошибка: {e}")
    finally:
        db.close()


if __name__ == "__main__":
    seed_knowledge_base()